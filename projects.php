    <p>В каждый учебный день курсам  посвящается две учебных пары после завтрака. Таким образом, каждый участник  Дельты
        может выбрать себе два курса, которые он будет слушать ближайшие три  дня. В следующие трёхдневки анонсируются
        новые курсы, и участник вновь может  сделать выбор.</p>
    <p>Каждый курс рассчитан на 6-10 человек. Если на  какой-то курс записывается больше участников, предпочтение отдаётся
        тем, кто  лучше написал вступительную олимпиаду.</p>
    <p><strong>Проект</strong>, в отличие от курса,  выбирается один раз в начале и длится все две недели, включая дни
        отдыха. В каждом  проекте может участвовать от 5 до 15 человек. Участники каждого проекта делают  его совместно,
        распределяя между собой задачи и роли. В конце лагеря проходит  презентация готовых проектов. Некоторые
        проекты прошлых лет:</p>
