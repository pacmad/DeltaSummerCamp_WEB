    <h1>Место проведения</h1>

    <p>GOROD – уникальное объединение, один из важнейших центров русскоязычной культуры в Германии, собирающий под своей
        крышей граждан Германии, России, Украины, Грузии, Латвии, Армении и многих других стран. Официальный язык лагеря
        русский, но также приветствуется общение на английском и немецком языках.</p>

    <h1>Проживание и питание</h1>
    <div onclick="showMore(this)">
        <p>Центр располагается в центральной части Мюнхена (до центральной площади 40-50 минут пешком
            или 15 минут на метро),
            в очень тихом, спокойном районе. В одном квартале от центра расположен огромный прекрасный парк (на
            <a href="https://www.google.com/maps/place/Hansastra%C3%9Fe+181,+81373+M%C3%BCnchen,+Germany/@48.1219181,11.5286315,17z/data=!4m5!3m4!1s0x479dd8b5e31826f3:0x2fa6785644dd78bd!8m2!3d48.1193537!4d11.5349835?hl=en-US"
               target="_blank">карте</a> слева вверху).
            Центр занимает два этажа уютного 3-этажного здания, остальные помещения в том же здании снимают небольшие творческие
            компании.</p>
        <p>В Центре оборудовано достаточное количество спален. В большой спальне, разделённой на отсеки по 4-6 спальных мест,
            проживает 30 детей, в остальных спальнях,
            некоторые из которых разделены на два отсека, размещаются по 8-12 детей. Девочки и мальчики проживают
            отдельно. Также мы стараемся разделять ребят по возрастам. Кровати двухэтажные. Рядом с кроватями есть
            небольшие тумбочки для хранения личных вещей. Недалеко от спален расположены душевые и туалетные комнаты.<p>
        <p>В центре GOROD своя кухня (готовят сотрудники Центра) и столовая. Питание четырёхразовое. При организации питания
            мы учитываем информацию о аллергиях, сотрудники кухни предупреждают детей о содержании возможных аллергенов.
            Всегда есть доступ к горячей воде и чаю.</p>
