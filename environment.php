        <h1>Досуг и отдых</h1>
        <p>В десяти минутах ходьбы от центра GOROD расположен прекрасный парк. Каждый день после занятий все участники
            лагеря выходят туда гулять после занятий, играть в футбол, волейбол, бадминтон и другие подвижные игры.</p>
        <p>Актовый зал, танц-зал и большое количество других помещений позволяют организовать разнообразный вечерний досуг.</p>
        <p>В дни отдыха (три дня за смену - две субботы и вторая среда) устраиваются экскурсии в музеи, зоопарк или
        выход в горы.</p>
